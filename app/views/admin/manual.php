<h3 id="a104">klíčová slova / vyhledávače</h3>
<p>
	Tato kapitola by mohla zabrat svůj vlastní manuál, proto jen ve zkratce:<br />
	Důležité jsou kolonky <b>description</b> u podstránek, které by měly obsahovat klíčová slova,
	ale zároveň ve smysluplných větách. Měli by to být cca tři věty, proto je jasné, že se nedá
	vyjmenovat vše, ale jen to co předpokládám že bude klient psát do vyhledávače.<br />
	Dále jsou důležité položky Titulek, titulek-seo, nebo název reference. Na Těchto pár slov berou vyhledávače
	nejvíce zřetel, a objeví se jak v záhlaví stránky, tak v adrese, a proto volte správná klíčová slova.
</p>



